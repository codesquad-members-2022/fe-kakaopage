const rankBox = document.querySelector(".main__dailyRank__content");
const dailyRank_tab = document.querySelector(".main__dailyRank__day");

dailyRank_tab.addEventListener("click", (evt) => {
  const target = evt.target;
  const tabNum = target.getAttribute("data-tabnum");
  changeTab(target, rankBox, tabNum);
});

const changeTab = (target, rankBox, tabNum) => {
  rankBox.innerHTML = tabContent[tabNum];

  const elems = document.querySelectorAll(".tab_list");

  [...elems].forEach((elem) => {
    if (elem.className === "tab_list tab_active") {
      elem.className = "tab_list";
    }
  });

  target.className = "tab_list tab_active";
};

const nextBtn = document.querySelector(".button__next");
const prevBtn = document.querySelector(".button__previous");
const slider = document.querySelector(".slider-list");
const sliderContents = document.querySelectorAll(".slider-list img");

let curIndex = 0;

const showNext = () => {
  const image = slider;
  const Time = 300;
  if (curIndex < sliderContents.length - 1)
    setTimeout(() => {
      image.style.transform = "translateX(-" + (curIndex + 1) * 720 + "px)";
      curIndex++;
    }, Time);
};

const showPrev = () => {
  const image = slider;
  const Time = 300;
  if (curIndex > 0)
    setTimeout(() => {
      image.style.transform = "translateX(-" + (curIndex - 1) * 720 + "px)";
      curIndex--;
    }, Time);
};

nextBtn.addEventListener("click", (evt) => {
  showNext();
});

prevBtn.addEventListener("click", (evt) => {
  showPrev();
});
